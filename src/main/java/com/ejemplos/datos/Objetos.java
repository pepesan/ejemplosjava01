package com.ejemplos.datos;

public class Objetos {
    public static void main(String[] args) {
        // define los diferentes tipos de objetos
        // basados en variables prinmitivas
        // variable de tipo booleano
        Boolean b = Boolean.TRUE;
        System.out.println(b);
        // Variable de tipo Byte
        Byte byt = 1;
        System.out.println(byt);
        // Variable de tipo Entero
        Integer i = 1;
        System.out.println(i);
        // Variable de tipo Largo
        Long l = 1L;
        System.out.println(l);
        // Variable de tipo Char
        Character c = 'a';
        System.out.println(c);
        // Variable de tipo corto
        Short s = (short) 1;
        System.out.println(s);
        // Variable de tipo Float
        Float f = 1.0f;
        System.out.println(f);
        // Variable de tipo Double
        Double d = 1.0;
        System.out.println(d);
        // Variable de tipo Void
        Void  v = null;
        System.out.println(v);
    }
}
